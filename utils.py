def check_team_abbr(abr):
    if abr == 'BKN':
        return 'BRK'
    if abr == 'CHA':
        return 'CHO'
    if abr == 'PHO':
        return 'PHX'
    if abr == 'NO':
        return 'NOP'
    if abr == 'NY':
        return 'NYK'
    if abr == 'SA':
        return 'SAS'
    if abr == 'GS':
        return 'GSW'
    return abr
